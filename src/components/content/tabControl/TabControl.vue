<template>
    <div class="tab-control">
        <div v-for="(item,index) in titles"
             :class="{active: index === currentIndex}"
             @click="itemClick(index)"
             class="tab-item">
            <span>{{item}}</span>
        </div>
    </div>
</template>

<script>
    export default {
        name: "TabControl",
        props: {
            titles: {
                type: Array,
                default() {
                    return []
                }
            }
        },
        data(){
            return{
                currentIndex: 0
            }
        },
        methods:{
            itemClick(index){
                this.currentIndex = index
                this.$emit('tabClick',index)
            }
        }
    }
</script>

<style scoped>
    .tab-control{
        display: flex;
        text-align: center;
        font-size: 18px;
        line-height: 35px;
        height: 35px;
        background-color: #fff;
        z-index: 5;
    }
    .tab-item{
        flex: 1;
    }
    .active{
        color: var(--color-high-text);
    }
    .active span{
        border-bottom: 2px solid #ff5777;
        padding: 3px 5px
    }
</style>